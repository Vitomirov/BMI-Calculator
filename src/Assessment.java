public class Assessment extends Calculator {

    public Assessment(int height, int weight) {
        super(height, weight);
    }

    public String getBMIStatus() {
        double bmi = calculateBMI();
        if (bmi < 18.5) {
            return "Underweight";
        } else if (bmi < 25) {
            return "Normal";
        } else if (bmi < 30) {
            return "Overweight";
        } else {
            return "Obese";
        }
    }
}
